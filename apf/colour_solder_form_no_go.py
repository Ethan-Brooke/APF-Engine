_TOL = 1e-9
_W = cmath.exp(2j * cmath.pi / 3)


def _mm(a, b):
    return [[sum(a[i][k] * b[k][j] for k in range(3)) for j in range(3)]
            for i in range(3)]


def _eq_mod_phase(a, b):
    lam = None
    for i in range(3):
        for j in range(3):
            if abs(b[i][j]) > _TOL:
                lam = a[i][j] / b[i][j]
                break
        if lam is not None:
            break
    if lam is None or abs(abs(lam) - 1.0) > _TOL:
        return False
    return all(abs(a[i][j] - lam * b[i][j]) < _TOL
               for i in range(3) for j in range(3))


