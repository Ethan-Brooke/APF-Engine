def _moebius(E, universe):
    """Coefficients j(T), T nonempty, with E(S) = sum_{T subset S} j(T); unique."""
    j = {}
    for r in range(1, len(universe) + 1):
        for T in itertools.combinations(sorted(universe), r):
            T = frozenset(T)
            j[T] = E[T] - sum(j[U] for U in j if U < T)
    return j


class _LedgerWorld:
    """Multi-interface cost world with the clause's operation set.

    Operation set at interface g (THE CLAUSE): delete any nonzero ledger line billed
    solely at g (footprint or joint), target state globally admissible. Multi-billed
    joint lines are deletable by no single observer (L_irr check 4 simultaneity).
    op_set='none'/'footprint_only' model the withheld-clause probes (lower bound)."""

    def __init__(self, tables, cap, universe, op_set="clause"):
        """op_set: 'clause' (own-billing modifications, the lemma's operation set),
        'footprint_only' (joint-line deletions withheld -- the CM2-necessity probe),
        'none' (all deletions withheld -- the CM1-necessity probe)."""
        self.universe = frozenset(universe)
        self.j = {g: _moebius(E, universe) for g, E in tables.items()}
        self.cap = cap
        self.op_set = op_set

    def energy(self, g, S):
        return sum(v for T, v in self.j[g].items() if T <= S)

    def total_energy(self, S):
        return sum(self.energy(g, S) for g in self.j)

    def admissible(self, S):
        return all(self.energy(g, S) <= self.cap for g in self.j)

    def line_owners(self, T):
        return [g for g in self.j if self.j[g].get(T, 0) != 0]

    def removal_is_local(self, d, S):
        """Is the state change S -> S\\{d} performable by single-interface observers
        under the clause? Yes iff every nonzero deleted line is single-billed."""
        if self.op_set == "none":
            return False
        for T in {T for g in self.j for T, v in self.j[g].items()
                  if v != 0 and d in T and T <= S}:
            if len(T) >= 2:
                if self.op_set == "footprint_only":
                    return False  # CM2-necessity probe: joint deletions withheld
                if len(self.line_owners(T)) >= 2:
                    return False
        return self.admissible(S - {d})

    def delta_cross_zero(self, d):
        """Billing-locus coupling: no joint line containing d billed at >=2 interfaces."""
        joints = {T for g in self.j for T, v in self.j[g].items()
                  if v != 0 and d in T and len(T) >= 2}
        return all(len(self.line_owners(T)) < 2 for T in joints)

    def reachable_floor(self, S0, record_part):
        """Min total energy over states reachable from S0 via clause operations,
        staying in the record class (record part held)."""
        seen, frontier = {S0}, [S0]
        best = self.total_energy(S0)
        while frontier:
            S = frontier.pop()
            best = min(best, self.total_energy(S))
            for d in S - record_part:
                if self.removal_is_local(d, S):
                    T = S - {d}
                    if T not in seen:
                        seen.add(T)
                        frontier.append(T)
        return best


