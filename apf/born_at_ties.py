# ------------------------------------------------------------------ matrices
def _eye(n):
    return [[F(1) if i == j else F(0) for j in range(n)] for i in range(n)]


def _zeros(n):
    return [[F(0)] * n for _ in range(n)]


def _mm(A, B):
    n, m, p = len(A), len(B), len(B[0])
    return [[sum(A[i][k] * B[k][j] for k in range(m)) for j in range(p)]
            for i in range(n)]


def _madd(A, B):
    return [[A[i][j] + B[i][j] for j in range(len(A[0]))]
            for i in range(len(A))]


def _scal(c, A):
    return [[c * A[i][j] for j in range(len(A[0]))] for i in range(len(A))]


def _tr(A):
    return sum(A[i][i] for i in range(len(A)))


def _meq(A, B):
    return all(A[i][j] == B[i][j]
               for i in range(len(A)) for j in range(len(A[0])))


def _diagm(v):
    n = len(v)
    return [[v[i] if i == j else F(0) for j in range(n)] for i in range(n)]


def _perm_matrix(perm):
    n = len(perm)
    return [[F(1) if perm[j] == i else F(0) for j in range(n)]
            for i in range(n)]


def _conj(P, A):
    PT = [[P[j][i] for j in range(len(P))] for i in range(len(P))]
    return _mm(_mm(P, A), PT)


def _projectors(n):
    out = []
    for k in range(n):
        E = _zeros(n)
        E[k][k] = F(1)
        out.append(E)
    return out


def _dephase(rho, spec):
    out = _zeros(len(rho))
    for Q in spec:
        out = _madd(out, _mm(_mm(Q, rho), Q))
    return out


