def _items_to_data(items: Iterable[ProvenanceItem]) -> Dict[str, Dict]:
    return {
        x.name: {
            "kind": x.kind,
            "source": x.source,
            "imported_physical_content": x.imported_physical_content,
            "note": x.note,
        }
        for x in items
    }


def check_T_ACC_unification_all_P_predecessor() -> Dict:
    try:
        from apf.acc_unification_all_p import check_T_ACC_unification_all_P
        result = check_T_ACC_unification_all_P()
    except Exception as exc:
        return _fail(
            "check_T_ACC_unification_all_P_predecessor",
            status="FAIL",
            summary="Could not import/run consolidated all-P predecessor.",
            data={"error": repr(exc)},
        )

    if result.get("consistent") and result.get("status") == "P_cat_all_exported":
        return _ok(
            "check_T_ACC_unification_all_P_predecessor",
            status="P_cat_all_exported",
            summary="Consolidated all-P categorical predecessor passes.",
            data={"predecessor_status": result.get("status")},
            dependencies=["check_T_ACC_unification_all_P"],
        )
    return _fail(
        "check_T_ACC_unification_all_P_predecessor",
        status="FAIL",
        summary="Consolidated all-P predecessor did not pass.",
        data={"predecessor": result},
    )


