# capacity/beta system recomputed exactly (mirrors the tiling module's own
# module-level functions, whose arity this check inspects bank-side)
def _C_vac(n): return 9 * n + 15
def _C_mat(n): return 6 * n + 1
def _C_tot(n): return 15 * n + 16
def _d_eff(n): return (_C_tot(n) - 1) + _C_vac(n)
def _six_b3(n): return 66 - 8 * n
def _six_b2(n): return 43 - 8 * n
def _six_bY(n): return F(40, 3) * n + 1


def _solves(n):
    return (_six_b3(n) == _C_vac(n) and _six_b2(n) == _C_mat(n)
            and _six_bY(n) == _d_eff(n) - _C_tot(n))


