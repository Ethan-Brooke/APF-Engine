def _bfs_dist(g, starts, targets):
    adj = {v: [] for v in g['V']}
    for (x, y) in g['E']:
        adj[x].append(y)
        adj[y].append(x)
    dist = {v: 0 for v in starts}
    frontier = list(starts)
    while frontier:
        for v in frontier:
            if v in targets:
                return dist[v]
        nxt = []
        for v in frontier:
            for u in adj[v]:
                if u not in dist:
                    dist[u] = dist[v] + 1
                    nxt.append(u)
        frontier = nxt
    return None


