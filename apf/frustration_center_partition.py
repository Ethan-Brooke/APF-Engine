class _PW:
    """Two-element pair world.  kind: 'string' (partner-absent defence =
    anchor + the L billed slots -- the computed electric-argmin where no
    bounded option exists), 'glue' (anchor + glue -- the screen-glue
    billing where a bounded screen exists), 'argmin' (cheaper of the
    two -- ruling 4a read directly), 'abovemin' (the REVERSED setting --
    dearer of the two; the 4a anti-rigging control).  pp_discount=False
    is the parent's Rule A on even classes (partner-present support
    keeps the glue -- configuration-independent)."""

    def __init__(self, kind, L, ac, sc, gc, pp_discount=True):
        self.kind, self.L = kind, L
        self.cost = {('a', d): ac[d] for d in _D2}
        for d in _D2:
            self.cost[('g', d)] = gc[d]
            for k in range(L):
                self.cost[('s', d, k)] = sc[d]

    def M(self, S):
        return frozenset(('a', d) for d in S)

    def T(self, S):
        return frozenset(('p', d) for d in S)

    def _stringset(self, d):
        return (frozenset({('a', d)})
                | frozenset(('s', d, k) for k in range(self.L)))

    def _glueset(self, d):
        return frozenset({('a', d), ('g', d)})

    def W(self, p, S):
        _, d = p
        if _PARTNER[d] in S and getattr(self, 'pp', True):
            return frozenset({('a', d)})
        if self.kind == 'string':
            return self._stringset(d)
        if self.kind == 'glue':
            return self._glueset(d)
        cs = self.c(self._stringset(d))
        cg = self.c(self._glueset(d))
        if self.kind == 'argmin':
            return self._stringset(d) if cs < cg else self._glueset(d)
        return self._stringset(d) if cs >= cg else self._glueset(d)

    def escaped(self, S):
        out = set()
        for p in self.T(S):
            out |= self.W(p, S)
        return frozenset(out) - self.M(S)

    def c(self, dirs):
        return sum((self.cost[x] for x in dirs), F(0))

    def bill(self, S):
        return self.c(self.M(S)) + self.c(self.escaped(S))


