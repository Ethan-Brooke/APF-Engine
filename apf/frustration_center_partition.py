def _component(w, C, start, gate_removals):
    if gate_removals and w.bill(start) > C:
        return frozenset()
    seen = {start}
    q = deque([start])
    while q:
        u = q.popleft()
        for d in _D2:
            if d in u:
                v = u - {d}
                if (not gate_removals or w.bill(v) <= C) and v not in seen:
                    seen.add(v)
                    q.append(v)
            else:
                v = u | {d}
                if w.bill(v) <= C and v not in seen:
                    seen.add(v)
                    q.append(v)
    return frozenset(seen)


