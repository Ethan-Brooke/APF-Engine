def _model(inv_ranges, n_red):
    axes = [range(k) for k in inv_ranges] + [range(n_red)]
    X = [tuple(p) for p in itertools.product(*axes)]
    def g(x):
        return x[:-1] + ((x[-1] + 1) % n_red,)
    def orbit(x):
        out, y = [], x
        for _ in range(n_red):
            out.append(y)
            y = g(y)
        return frozenset(out)
    return X, g, orbit


def _binary_partitions(X):
    n = len(X)
    for mask in range(1, 2 ** n - 1):
        yield frozenset(X[i] for i in range(n) if (mask >> i) & 1)


def _is_invariant(side, orbit_map):
    return all((orb <= side) or not (orb & side) for orb in orbit_map)


